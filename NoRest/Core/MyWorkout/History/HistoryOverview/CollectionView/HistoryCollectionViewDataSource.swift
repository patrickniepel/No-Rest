import UIKit

extension HistoryTableViewCell: UICollectionViewDataSource {
    
    func numberOfSections(in collectionView: UICollectionView) -> Int {
        return 1
    }
    
    func collectionView(_ collectionView: UICollectionView, numberOfItemsInSection section: Int) -> Int {
        return workouts.count
    }
    
    func collectionView(_ collectionView: UICollectionView, cellForItemAt indexPath: IndexPath) -> UICollectionViewCell {
        let cell = collectionView.dequeueReusableCell(withReuseIdentifier: NRConstants.CellIdentifiers.historyCollectionViewCell, for: indexPath) as? HistoryCollectionViewCell
        let date = workouts[indexPath.row].date
        cell?.setup(date: date)
        return cell ?? UICollectionViewCell()
    }
}
